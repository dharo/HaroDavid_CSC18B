    //regex functions
    public static boolean fname(String check){
        return check.matches("[A-Z][a-zA-Z]*");
    }
    public static boolean isEmail(String check){
        return check.matches("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
		+ "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
    }
    
    
    
    
