    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //check to see if input was made
            System.out.println(jTextField1.getText());
            System.out.println(jTextField2.getText());
            //verify if input is valid
            boolean nameisValid = fname(jTextField1.getText());
            boolean emailValid = isEmail(jTextField2.getText());
            //Check if name is valid!
            System.out.println(nameisValid);
            System.out.println(emailValid);
            
            //display next screen if valid input
            if(nameisValid&&emailValid){
                PlayUI.setVisible(false);
                GameUI.setVisible(true);
                jLabel4.setText("Hey "+jTextField1.getText()+", Let's Play!");
                jLabel4.setVisible(true);
            }
            //handle '!' marker for bad input
            if(nameisValid != true){
                jLabel5.setVisible(true);
            }
            if(emailValid != true){
                jLabel6.setVisible(true);
            }
            if(nameisValid == true){
                jLabel5.setVisible(false);
            }
            if(emailValid == true){
                jLabel6.setVisible(false);
            }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

